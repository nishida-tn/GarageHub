package com.hsgaragepecas.garagehub.data.local.order

/**
 * Represents the status of an order.
 */
enum class OrderStatus {
    /**
     * The order has been completed.
     */
    CONCLUIDO,
    /**
     * The order is open.
     */
    ABERTO,
    /**
     * The order has been cancelled.
     */
    CANCELADO
}
